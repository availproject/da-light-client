// Just a wrapper function, to be used when spawning threads for verifying proofs
// for a certain block
fn kc_verify_proof_wrapper(
    row: u16,
    col: u16,
    block: u64,
    total_rows: usize,
    total_cols: usize,
    proof: &Vec<u8>,
    commitment: &Vec<u8>,
) -> bool {
    let status = kc_verify_proof(col, proof, commitment, total_rows, total_cols);
    if status {
        println!("\n➕  Verified cell ({:>3}, {:>3}) ", row, col );
    } else {
        println!("\n❌  Failed for cell ({:>3}, {:>3}) ", row, col);
    }

    status
}

pub fn verify_proof(
    total_rows: u16,
    total_cols: u16,
    cells: &Vec<Cell>,
    commitment: &Vec<u8>
) -> u32 {
    let mut count: u32 = 0;

    for cell in cells {
        let block = cell.block;
        let row = cell.row;
        let col = cell.col;
        let _proof = &cell.proof;
        let c_start = usize::from(row) * 48;
        let c_end = c_start + 48;
        let _commitment = &commitment[c_start..c_end].to_vec();

        if kc_verify_proof_wrapper(
            row,
            col,
            block,
            total_rows.into(),
            total_cols.into(),
            _proof,
            _commitment,
        ) {
            count+=1;
        }
    }

    count
}